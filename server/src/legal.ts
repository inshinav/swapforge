export type LegalSlug = 'terms' | 'privacy' | 'acceptable-use';

const SLUGS: LegalSlug[] = ['terms', 'privacy', 'acceptable-use'];

export function isLegalSlug(s: string): s is LegalSlug {
  return (SLUGS as string[]).includes(s);
}

